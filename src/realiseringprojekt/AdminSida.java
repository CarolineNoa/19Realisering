    private void btnUtrustningRaderingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUtrustningRaderingActionPerformed
        // Knapp för att ta bort utrustning
        
        String utrustningRuta = txtSkrivUtrustning.getText();
        String fraga = "SELECT Utrustnings_ID, Benamning FROM utrustning";
        boolean hittad = false;
        
        try {            
            ArrayList<HashMap<String, String>> utrustningar = idb.fetchRows(fraga);
            
            for(HashMap<String, String> enUtrustning : utrustningar) {
                String utrustningNamn = enUtrustning.get("Benamning");
                String id = enUtrustning.get("Utrustnings_ID");
                                        
                if(!utrustningRuta.isEmpty() && utrustningRuta.equalsIgnoreCase(utrustningNamn)) {
                    //Kallar på radera-metoden i samma klass
                    radera(id);            
                    lblUtrustningRaderad.setText(utrustningNamn + " har raderats");
                    hittad = true;
                }                
            }
            if(!hittad) {
            lblUtrustningRaderad.setText("Utrustningen existerar ej."); 
            }
        } 
        catch (InfException fel) {
            lblUtrustningRaderad.setText("Ett fel uppstod!");
        }    
    }//GEN-LAST:event_btnUtrustningRaderingActionPerformed

    
    private void radera(String id) {
        //Alla kallelser till databasen för radering av utrustning sker genom delete-metoden        
        try {
        idb.delete("DELETE FROM kommunikation WHERE Utrustnings_ID=" + id);
        idb.delete("DELETE FROM teknik WHERE Utrustnings_ID=" + id);
        idb.delete("DELETE FROM vapen WHERE Utrustnings_ID=" + id);
        idb.delete("DELETE FROM innehar_utrustning WHERE Utrustnings_ID=" + id);           
        idb.delete("DELETE FROM utrustning WHERE Utrustnings_ID=" + id); 
        }     
        catch (InfException fel){
          lblUtrustningRaderad.setText("Ett fel uppstod vid borttagning av utrustning!");
        }
    }
        
