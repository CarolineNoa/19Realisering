    private boolean finnsAlienRegistreringAgent() {
        //Denna metod tittar på ifall agentens ID som angavs i Alien-registrering existerar
        ArrayList<String> agentID = new ArrayList<String>();
        boolean hittad = false;
        String angivenAgentID = txtAlienAgent.getText();
        
        try {
            agentID = idb.fetchColumn("SELECT AGENT_ID FROM agent");
            
            for(String enAgentID : agentID) {
                if(enAgentID.equals(angivenAgentID)) {
                hittad = true;
                }
            }
        }       
        catch(InfException fel) {
            lblAlienFelMeddelande.setText("Ett fel uppstod med agenten som angavs!");
        }

        if(!hittad) {
            lblAlienFelMeddelande.setText("Agenten som angavs existerar ej!");
        }
        
        return hittad;
    }
    
    
